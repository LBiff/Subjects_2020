#include <stdio.h>

int main(){
    int a[8] = {1,2,3,4,5,6,7};
    int * p = a; //*p = 1
    p++; //*p = 2
    //根据优先级，[]优先级 > *(取值运算) > *(乘法)
    // 1. 计算[], 因为[]前面必须是数组指针，只能和(p+1)结合
    // 2. 则先计算(p + 1), p+1指向3, 则(p + 1)[2]指向5
    // 第2个*只能被解释成乘号
    //前面的*(p+1) = 3
    //输出15
    // 原式：*(p+1)*(p+1)[2]
    int ret = *(p + 1)*((p + 1)[2]);
    printf("ret= %d", ret);
}